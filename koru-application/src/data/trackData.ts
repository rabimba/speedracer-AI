export const TRACKS: Track[] = [SONOMA_RACEWAY, THUNDERHILL_EAST];
export const DEFAULT_TRACK = SONOMA_RACEWAY;

export function getTrackByName(name?: string | null): Track {
  if (!name) return DEFAULT_TRACK;
  const exact = TRACKS.find((track) => track.name === name);
  if (exact) return exact;

  const normalized = name.toLowerCase();
  const fuzzy = TRACKS.find((track) => normalized.includes(track.name.toLowerCase().split(' ')[0]));
  return fuzzy ?? DEFAULT_TRACK;
}
